let languages = ["Ruby", "Python", "Swift"]
languages.contains("Swift")

extension String {
    func containsAny(of array: [String]) -> Bool {
        for item in array {
            if self.contains(item) {
                return true
            }
        }
        return false
    }
}

anotherInput.containsAny(of: languages)
languages.contains(where: anotherInput.contains)

    
